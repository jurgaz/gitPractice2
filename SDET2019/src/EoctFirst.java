
public class EoctFirst {

	public static void main(String[] args) {
		
		{	//switch statement
		
		char grade = 'B';
		
		switch (grade) {
		case 'A':
		System.out.println("Good Job");
		break;
		
		case 'B':
		System.out.println("B is Fine");
		break;
		default:                         //if none of the cases are true, execute default
			System.out.println("we know what's your grade");
			break;
		}
		
		}
		
		
		
	}

}
